
def triangle(base, height):
    '''
    return area of triangle

    '''
    area = 0.5 * base * height
    return area


def square(lenth):
    '''
    return area of triangle
    '''
    area = lenth * lenth
    return area

def rectangle(lenth, width):
    '''
    return area of rectangle
    '''
    area = lenth * width
    return area

